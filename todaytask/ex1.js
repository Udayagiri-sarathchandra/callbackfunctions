$(document).ready(function () {

    $('#btn1').click(function () {
        $('#name').css('display', 'block')
        $('#span1').css('display', 'flex')
    })

    $('#span1').click(function () {
        $('#name').css('display', 'none')
        $('#span1').css('display', 'none')
    })

})

